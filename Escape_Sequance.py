# 1st (\n)
print("Hello ,\nStudent")

# 2nd (\t)
print("Hello,\tWorld")

# 3rd (\\)
print("This is a Backslash : \\")

# 4th (\')
print("It\'s a Greate Date")

# 5th (\")
print("She Said, \"Hello\" ")

# 6th (\u or \U or \N{})
print("unicode character : \u03A9")
print("Unicode Character : \U0001F600")
print("Unicode character : \N{GREEK CAPITAL LETTER OMEGA}")

# 7th (\000)
print("Ocatal Value :\101")

# 8th (\xhh)
print("Hex value : \x41")

# 9th (\r)
print("Hello, \rWorld")

# 10th (\b)
print('Hello,\b World!')

# 11th (\f)
print("Hello,\fWorld")
